func decodeManufacturer(edt []byte) string {
	if len(edt) != 3 {
		return ""
	}
	code := uint32(edt[0])<<16 | uint32(edt[1])<<8 | uint32(edt[2])
	if name, ok := manufacturerNames[code]; ok {
		return name
	}
	return fmt.Sprintf("0x%06X", code)
}
